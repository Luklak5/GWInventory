package com.luklak.inventory;

public class Material {
	private int id;
	private int category;
	private String binding;
	private int count;
	
	public int getId() {
		return id;
	}
	public int getCategory() {
		return category;
	}
	public String getBinding() {
		return binding;
	}
	public int getCount() {
		return count;
	}
	
	
}
